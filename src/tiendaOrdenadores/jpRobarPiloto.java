    public jpRobarPiloto(int idEscuderia) {
        initComponents();
        idEsc= idEscuderia-1;
        int aux=0;
        for (Escuderia escuderia : Formula1UI.formula1.escuderias){
            if(aux != idEscuderia-1)
                jCEscuderias.addItem(escuderia.getNombre());
            ++aux;
        }
        
        int primerId = 0;
        if (idEscuderia-1 == 0) ++primerId;
        jCId.removeAllItems();
        if(Formula1UI.formula1.escuderias.get(primerId).getNumOficiales() == 0){
            jCId.addItem(0);
        }else{
            for (int num =1; num < Formula1UI.formula1.escuderias.get(primerId).getNumOficiales()+1; ++num){
                jCId.addItem(num);
            }
        }
        
        listapilotos.setText(Formula1UI.formula1.escuderias.get(primerId).mostrarOficiales()
         +"El numero total de pilotos es: " + Formula1UI.formula1.escuderias.get(primerId).getNumOficiales());
    }


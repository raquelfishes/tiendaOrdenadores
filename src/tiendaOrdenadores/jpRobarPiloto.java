    private void jCEscuderiasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jCEscuderiasActionPerformed
        jCId.removeAllItems();
        int id = jCEscuderias.getSelectedIndex();
        if (id >= idEsc) ++id;
        
        if(jCOficial.isSelected()){
            if(Formula1UI.formula1.escuderias.get(id).getNumOficiales() == 0){
                jCId.addItem(0);
            }else{
                for (int num =1; num < Formula1UI.formula1.escuderias.get(id).getNumOficiales()+1; ++num){
                    jCId.addItem(num);
                }
            }
            listapilotos.setText(Formula1UI.formula1.escuderias.get(id).mostrarOficiales()
             +"El numero total de pilotos es: " + Formula1UI.formula1.escuderias.get(id).getNumOficiales());
        }else{
            if(Formula1UI.formula1.escuderias.get(id).getNumProbadores() == 0){
                jCId.addItem(0);
            }else{
                for (int num =1; num < Formula1UI.formula1.escuderias.get(id).getNumProbadores()+1; ++num){
                    jCId.addItem(num);
                }
            }
            listapilotos.setText(Formula1UI.formula1.escuderias.get(id).mostrarProbadores()
             +"El numero total de pilotos es: " + Formula1UI.formula1.escuderias.get(id).getNumProbadores());
        }
    }//GEN-LAST:event_jCEscuderiasActionPerformed

